/** Ray-casting point-in-polygon test */
function pointInPolygon(x: number, y: number, poly: number[][]): boolean {
  let inside = false;
  for (let i = 0, j = poly.length - 1; i < poly.length; j = i++) {
    const xi = poly[i][0],
      yi = poly[i][1];
    const xj = poly[j][0],
      yj = poly[j][1];
    if (yi > y !== yj > y && x < ((xj - xi) * (y - yi)) / (yj - yi) + xi) {
      inside = !inside;
    }
  }
  return inside;
}

/**
 * Find which muscle group contains the given point.
 * Coordinates are normalized 0-1 fractions of the SVG viewBox.
 * Returns the muscle slug or 0 if the point is not inside any muscle.
 */
export function findMuscleAtPoint(
  nx: number,
  ny: number,
  side: 'front' | 'back',
  tappableSlugs: Set<string>
): string | null {
  const polygons = side === 'front' ? FRONT_POLYGONS : BACK_POLYGONS;
  for (const [slug, polys] of Object.entries(polygons)) {
    if (!tappableSlugs.has(slug)) continue;
    for (const poly of polys) {
      if (pointInPolygon(nx, ny, poly)) return slug;
    }
  }
  return null;
}
